#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dup;
	size_t	len;
	int		i;

	i = 0;
	len = ft_strlen(s);
	dup = (char *)malloc(len + 1);
	if (dup == NULL)
		return (NULL);
	while (*s)
	{
		dup[i] = *s++;
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
